public class User//用户对象
{
    String account;//用户账号
    String password;//用户密码
    String name;//用户姓名
    String phone;//用户手机号
    User(String account,String password,String name,String phone)
    {
        this.account=account;
        this.password=password;
        this.name=name;
        this.phone=phone;
    }
}
